#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	a;

	a = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (a < n && s2[a] != '\0')
	{
		s1[i] = s2[a];
		i++;
		a++;
	}
	s1[i] = '\0';
	return (s1);
}
