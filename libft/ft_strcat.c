#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int a;

	a = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[a] != '\0')
	{
		s1[i] = s2[a];
		a++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
